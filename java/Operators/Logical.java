package Operators;

public class Logical {
    public static void main(String[] args) {
        // logical and 
        System.out.println((3 > 2)&&(5 < 6));  // true 
        // logical or
        System.out.println((5 > 2)||(3 < 1));   // true
        //logical not
        System.out.println(!(5 < 6));            // false
    }
}
