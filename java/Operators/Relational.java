package Operators;

public class Relational {
    public static void main(String[] args) {
        //equal
        int a = 10;
        int b = 5;
        System.out.println((a==b));       // false 

        // not equal
         System.out.println((a != b));      // true 
       // greterthan
       System.out.println((a > b));         // true 
       //lessthan
       System.out.println((a < b ));        // false 
       // grerterthan equal
       System.out.println((a >= b));        // true
       //lessthan equal 
       System.out.println(a <= b);          // flase
    }
}
